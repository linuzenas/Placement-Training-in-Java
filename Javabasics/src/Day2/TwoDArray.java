package Day2;

public class TwoDArray {

    public static void main(String[] args) {

        int[][] matrix = {
                {10, 20, 30},
                {40, 50, 60},
                {70, 80, 90}
        };

        System.out.println(matrix[0][0]);
        System.out.println(matrix[1][2]);
        System.out.println(matrix[2][1]);

    }
}